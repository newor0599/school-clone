function searchTeacher(){
  input = document.getElementById("search-bar")
  if (input.value != ""){
    filter = input.value.toUpperCase();
    name_list = document.querySelectorAll("#t-frame");
    temp_name = []
    for (i=0;i<name_list.length;i++){
      temp_name.push(name_list[i].innerText)
    }
    name_list = temp_name
    for (i=0;i<name_list.length;i++){
      if (name_list[i].toUpperCase().indexOf(filter)>-1){
        document.querySelectorAll("#t-frame")[i].style.display = "flex";
      }
        else{
        document.querySelectorAll("#t-frame")[i].style.display = "none";
      }
    }
  
    }
}

